# program that converts temperature from celsius to fahrenheit

# step 1. ask for user input for the temperature in celsius
temperature = int(input("What is the temperature in Celsius? "))

# step 2. convert the temperature from celsius to fahrenheit and assign it to a variable "new_temp"
new_temp = (temperature*1.8)+32

# step 3. print out "The temperature in Fahrenheit is: ..."
print("The temperature in Farenheit is:")
print(new_temp)

